#Menentukan bilangan positif, negatif, atau nol

#-menginput angka
angka = float(input("Tulis Sebuah Angka: "))
 
#-menampilkan kondisi angka positif
if angka > 0:
   print("Angka Positif")
 
#-menampilkan kondisi angka nol   
elif angka == 0:
   print("Angka Nol")
 
#-menampilkan kondisi angka negatif
else:
   print("Angka Negatif") 
